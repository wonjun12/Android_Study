package com.wonjun.training05_phone_listview.model;

public class NamePhone {
    private String name;
    private String phone;

    public NamePhone(String name, String phone) {
        this.name = name;
        this.phone = phone;
    }

    public String getName() {
        return name;
    }

    public String getPhone() {
        return phone;
    }
}
